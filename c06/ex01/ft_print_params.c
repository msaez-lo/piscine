#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	a;

	a = 0;
	i = 1;
	while (i != argc)
	{
		a = 0;
		while (argv[i][a])
		{
			write(1, &argv[i][a], 1);
			a++;
		}
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
